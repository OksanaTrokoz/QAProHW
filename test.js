
function pow(x, y) {
    let result = 1; // Початкове значення
    for (let i = 0; i < y; i++) { // Цикл повторюється y разів
        result *= x; // Множимо result на x на кожній ітерації
    }
    return result; // Повертаємо остаточний результат
}

// Виклик функції та вивід результатів
console.log(pow(2, 3)); // Виведе 8



